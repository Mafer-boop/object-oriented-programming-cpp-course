#include <iostream>

using namespace std;

int main() {

    // Declaracion de variables
    float base = 5.5;
    float altura = 3.0;
    float area;

    // Calculo del area
    area = base * altura;

    // Mostrar resultado
    cout << "Base: " << base << endl;
    cout << "Altura: " << altura << endl;
    cout << "Area del rectangulo: " << area << endl;

    return 0;
}